<h1>Including Sets</h1>

<p class="lead">
    By including sets within your sets you can avoid having large, unmanageable
    question counts. First, copy the ID of the set you want to include by
    pressing the "copy" button:
    <br />
    <br />
    <img src="/help/include.png" alt="Copy ID button" style="width:80%;" />
    <br />
    <br />
    Then, paste the ID into the "Answer" field in the set editor and press "Tab"!
    <br />
    <br />
    <img src="/help/include2.png" alt="Copy ID button" style="width:80%;" />
    <br />
